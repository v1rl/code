import os

def generate_hack_1():
    """
    第一组数据：大 K，单点巨坑 (N=100000, K=50000)
    构造一个巨大的对称陷阱，让正向和反向遍历都在正中间完美跳过最优解。
    """
    n = 100000
    k = 50000
    a = [0] * n
    
    # 构造正向跳跃陷阱
    a[0] = 10000      # 第 1 个元素：诱导累加
    a[1] = -9999      # 第 2 个元素：抵消大部分值，但保持 sum > 0
    
    # 真正的最优解跨越 K 的边界
    a[k-1] = 6000     # 第 50000 个元素
    a[k] = 6000       # 第 50001 个元素
    
    # 构造反向跳跃陷阱（应对错解的 reverse 操作）
    a[2*k-2] = -9999  # 第 99999 个元素
    a[2*k-1] = 10000  # 第 100000 个元素

    with open("hack1.in", "w") as f:
        f.write(f"{n} {k}\n")
        f.write(" ".join(map(str, a)) + "\n")
    print("生成完毕: hack1.in (标程答案: 12000, 错解输出: 10000)")


def generate_hack_2():
    """
    第二组数据：小 K，高频连环坑 (N=100000, K=10)
    构造连续不断的周期性陷阱，反复触发错解的跳跃 Bug。
    """
    n = 100000
    k = 10
    a = []
    
    # 这是一个长度为 21 的周期 Block
    # 结构：[大正, 大负, ..., 最优解一半, 最优解一半, ..., 大负, 大正, 隔离墙]
    block = [
        10000, -9999, 0, 0, 0, 0, 0, 0, 0, 6000, # 前 10 个元素，促发 k 长度限制
        6000, 0, 0, 0, 0, 0, 0, 0, -9999, 10000, # 后 10 个对称元素，应对 reverse
        -10000                                   # 强行隔离两个 Block，防止跨块累加
    ]
    
    # 将 Block 填满 10^5 的数组
    while len(a) < n:
        a.extend(block)
    
    a = a[:n] # 截断到刚好 n 个元素

    with open("hack2.in", "w") as f:
        f.write(f"{n} {k}\n")
        f.write(" ".join(map(str, a)) + "\n")
    print("生成完毕: hack2.in (标程答案: 12000, 错解输出: 10000)")

if __name__ == "__main__":
    generate_hack_1()
    generate_hack_2()